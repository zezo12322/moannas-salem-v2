function getKindContext(kind: string) {
  return (
    kindContext[kind] ?? {
      role: "نشاط متخصص",
      method: "صمم النشاط بما يناسب موضوعه والفئة المستهدفة، مع التركيز على المعرفة العملية والتوثيق.",
      output: "خلاصات قابلة للبناء عليها داخل برامج مؤنث سالم وحملاتها المستقبلية.",
    }
  );
}

function audienceFor(activity: Activity) {
  const text = `${activity.title} ${activity.summary}`;

  if (text.includes("خريجات")) return "الخريجات الجدد والنساء في بداية مسارهن المهني.";
  if (text.includes("الأمهات") || text.includes("أم")) return "الأمهات العاملات والنساء اللاتي يواجهن ضغط الجمع بين العمل والرعاية.";
  if (text.includes("صحفيات") || text.includes("الصحفيات")) return "الصحفيات والعاملات في الإعلام، خصوصًا من يواجهن ضغطًا مهنيًا أو رقميًا.";
  if (text.includes("الصحفيين") || text.includes("الصحفيون")) return "الصحفيون والصحفيات والمهتمون بتطوير أدوات مهنية أكثر حساسية للنوع الاجتماعي.";
  if (text.includes("نساء") || text.includes("المرأة")) return "نساء عاملات وفاعلات في المجال العام، إلى جانب المهتمين بقضايا العدالة الجندرية.";

  return "الفئات المرتبطة بموضوع النشاط وشبكة مؤنث سالم من الصحفيات والنساء العاملات والشركاء.";
}

function documentationFor(activity: Activity) {
  if (activity.images && activity.imageType === "cards") {
    return `${activity.images} بطاقة/صورة من مواد الحملة`;
  }

  if (activity.images) {
    return `${activity.images} صور موثقة من النشاط`;
  }

  if (activity.fbLinks?.length) {
    return "تغطية منشورة على فيسبوك";
  }

  return "توثيق نصي ضمن سجل الأنشطة";
}

function makeLead(activity: Activity, campaign?: CampaignDetail) {
  const campaignText = campaign
    ? ` ويرتبط بملف ${campaign.title} كامتداد أوسع داخل عمل المؤسسة.`
    : "";

  return `هذه الصفحة تجمع الصورة الأوسع للنشاط: ما السياق الذي جاء منه، من يخاطب، ما الذي ركز عليه، وما المسار الذي يمكن البناء عليه بعده.${campaignText}`;
}

export function buildActivityInsight(
  activity: Activity,
  campaign?: CampaignDetail,
  postContent?: PostContentBlock
): ActivityInsight {
  const theme = themeContext[activity.theme];
  const kind = getKindContext(activity.kind);
  const themeMeta = THEME_META[activity.theme];
  const postMemoryText = postContent
    ? "تحتوي الصفحة أيضًا على خلاصة من البوستات المصاحبة."
    : "تظهر هنا خلاصة النشاط كما وردت في سجل مؤنث سالم.";

  const overview: ActivityOverviewItem[] = [
    {
      label: "نوع النشاط",
      value: activity.kind,
      helper: kind.role,
    },
    {
      label: "التاريخ",
      value: activity.dateLabel,
      helper: `ضمن سجل أنشطة ${activity.year}.`,
    },
    {
      label: "المجال",
      value: themeMeta.label,
      helper: theme.context,
    },
    {
      label: "الفئة الأقرب",
      value: audienceFor(activity),
      helper: "الفئة التي يخاطبها النشاط بصورة مباشرة أو غير مباشرة.",
    },
    {
      label: "التوثيق",
      value: documentationFor(activity),
      helper: postMemoryText,
    },
  ];

  if (activity.partners?.length) {
    overview.push({
      label: "الشراكات",
      value: activity.partners.join("، "),
      helper: "نفذ النشاط أو توسع أثره بالتعاون مع شركاء في المجال.",
    });
  }

  if (campaign) {
    overview.push({
      label: "الحملة المرتبطة",
      value: campaign.title,
      helper: "يمكن قراءة النشاط كجزء من مسار حملة أوسع.",
    });
  }

  const focusAreas = [
    kind.method,
    ...theme.focus,
    ...(postContent ? postContent.points.slice(0, 1) : []),
  ].slice(0, 5);

  const outcomes = [
    kind.output,
    ...theme.outcomes,
    ...(activity.fbLinks?.length
      ? ["إتاحة الرجوع إلى التغطية الأصلية على فيسبوك لمن يريد تتبع تفاصيل أكثر."]
      : []),
  ].slice(0, 5);

  const followUps: ActivityInsight["followUps"] = [];

  if (campaign) {
    followUps.push({
      label: campaign.title,
      href: `/campaigns/${campaign.slug}`,
      description: "قراءة ملف الحملة الكامل والسياق الحقوقي أو التوعوي المرتبط بهذا النشاط.",
    });
  }

  if (theme.resource) {
    followUps.push(theme.resource);
  }

  if (activity.fbLinks?.[0]) {
    followUps.push({
      label: "التغطية الأصلية",
      href: activity.fbLinks[0],
      description: "مشاهدة المنشور أو التغطية المنشورة على صفحة مؤنث سالم.",
      external: true,
    });
  }

  return {
    lead: makeLead(activity, campaign),
    overview,
    focusAreas,
    outcomes,
    followUps,
  };
}
